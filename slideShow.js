async function carousel() {
  const delay = async (ms) => {
    return new Promise((resolve) => {
      console.log("waiting for", ms);
      setInterval(resolve, ms);
    });
  };

  let divslideLen = divSlides.length;
  let slidtext = "";
  let sliddesc = "";
  for (let i = 0; i < divslideLen; i++) {
    slidtext = divSlides[i].text;
    sliddesc = divSlides[i].desc;
    document.getElementById("slides").innerHTML = slidtext;
    document.getElementById("slidesdesc").innerHTML = sliddesc;
    await delay(2000);

    if (i === divslideLen - 1) {
      i = 0;
    }
  }
}
